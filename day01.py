# 두수의 합
def solution(num1, num2):
    answer = num1 + num2
    return answer

# 두수의 차 
def solution(num1, num2):
    answer = num1-num2
    return answer

# 두수의 곱
def solution(num1, num2):
    answer = num1*num2
    return answer

# 몫 
def solution(num1, num2):
    answer = num1//num2
    return answer





